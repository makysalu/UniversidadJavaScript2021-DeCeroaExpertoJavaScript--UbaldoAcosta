let a = 3, b = 2;

//suma
let z = a + b;
console.log("Resultado de la suma: "+z);

//resta
z = a -b;
console.log("El resultado de la resta es: "+z);

//multiplicacion
z = a * b;
console.log("El resultado de la multiplicacion es: "+z);

//division
z = a /b;
console.log("El resultado de la division es: "+z);

//resto
z = a % b;
console.log("El resto de la division es: "+z);

//exponente
z = a**2;
console.log("El resultado exponente: "+ z);

//incremento
//preincremento
z = ++a;
console.log(z);
console.log(a);

//postincremento
z = b++;
console.log(z);
console.log(b);

//decremento
//predecremento
z = --a;
console.log(z);
console.log(a);

//postdecremento
z = b--;
console.log(z);
console.log(b);

